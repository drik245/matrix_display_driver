def get_icon(name):
    """Return icon bitmap by name. Returns all-on if not found."""
    return ICONS.get(name, bytes([0xFF] * 8))


def list_icons():
    """Print all available icon names."""
    names = sorted(ICONS.keys())
    print(f"Icons ({len(names)}):", ", ".join(names))
